public class Literals {
    public static void main(String[] args) {
        int bool = 0b101;
        int hex = 0x7E;
        int _form = 10_000_000;
        double engg_form1 = 12e-3;
        double engg_form2 = 12e4;
        System.out.println(bool);
        System.out.println(hex);
        System.out.println(_form);
        System.out.println(engg_form1);
        System.out.println(engg_form2);
    }
}
