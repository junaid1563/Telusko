public class DataTypes {
    public static void main(String[] args) {
        System.out.println("primitive ds");
        System.out.println("Integer, Float, Char, Boolean");

        byte by = 127;
        System.out.println(by);
        long ln = 23455l;
        System.out.println(ln);
        float num = 5.6f;
        System.out.println(num);

        // java supports UNICODE instead of ASCII
        char c = 'B';
        System.out.println(c);
    }
}
